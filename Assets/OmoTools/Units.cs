namespace OmoTools {
  
  public static class Units {

    // Distance
    /// <summary> Meters. </summary>
    public const float m  = 1.0F;
    /// <summary> Centimeters. </summary>
    public const float cm = 0.01F;
    /// <summary> Millimeters. </summary>
    public const float mm = 0.001F;

    // Time
    /// <summary> Seconds. </summary>
    public const float sec = 1.0F;
    /// <summary> Milliseconds. </summary>
    public const float ms  = 0.001F;
    /// <summary> Minutes. </summary>
    public const float min = 60.0F;
    /// <summary> Hours. </summary>
    public const float hr  = 3600.0F;

    // Force
    /// <summary> Newtons, AKA kg * meters * (meters / sec^2). </summary>
    public const float N = 1.0F;

  }

}